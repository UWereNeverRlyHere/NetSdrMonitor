namespace NetSdrMonitor.Core.Abstractions.Communication;

/// <summary>
/// Будує готовий до запуску монітор лінії під поточну конфігурацію. Реалізація живе в композиційному
/// корені (знає про настройки та обраний транспорт — мок чи реальний TCP), тож прикладний сервіс
/// створює свіжий монітор на кожен Start, не знаючи ні про WPF, ні про конкретний транспорт.
/// </summary>
public interface ISdrMonitorFactory
{
    /// <summary>
    /// Повертає новий, ще не запущений монітор, налаштований за актуальними параметрами.
    /// </summary>
    ISdrMonitor Create();
}
